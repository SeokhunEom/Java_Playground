public class Main {
    public static void main(String[] args) {
        System.out.println("6단");
        int i = 1;
        while (i < 10) {
            System.out.println(6 * i);
            i++;
        }

        System.out.println("7단");
        for(i = 1; i < 10; i++) {
            System.out.println(7 * i);
        }
    }
}